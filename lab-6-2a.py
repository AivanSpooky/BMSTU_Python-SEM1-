# 2a. Удалить элемент с заданным индексом с использованием
# любых средств Python.

# Ввод данных списка
while True:
    n = int(input("Введите размер списка: "))
    if n <= 0:
        print("Введите положительную длинну списка!")
    else:
        break
a = [0] * n
for i in range(n):
    a[i] = float(input(f"Введите {i+1}-ый элемент: "))

# Вводим индекс элемента, который необходимо удалить
while True:
    ind_elem = int(input(f"Укажите индекс элемента, который нужно удалить: "))
    if ind_elem <= 0:
        print("Введите положительный номер элемента!")
    elif ind_elem >= n+1:
        print("Вы ввели несуществующий номер элемента для данного списка!")
    else:
        break

# Удаляем из списка введенный номер элемента
a.pop(ind_elem-1)

# Выводим полученный список
print("\nПолученный список:")
for i, el in enumerate(a):
    print(f"{i+1}-ый элемент: {el}")